// Helper function to get category for a specific item
export const getCategoryForItem = (itemTitle) => {
    for (const category of categories) {
        if (category.items.some(item => item.title === itemTitle)) {
            return category.title;
        }
    }
    return "General Ailments";
};

// Helper function to get all glossary items as strings (for compatibility)
export const getGlossaryByCategory = () => {
    const glossaryByCategory = {};
    categories.forEach(category => {
        glossaryByCategory[category.title] = category.items.map(item => item.title);
    });
    return glossaryByCategory;
};